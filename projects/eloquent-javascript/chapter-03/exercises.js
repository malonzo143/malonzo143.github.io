//I: 2 numbers 
//O: smallest #
//C: N/A
//E: N/A
function min(a, b) { // function w/ a and b as params for numbers to be passed into 
   if (a < b) return a; // if stmt, a less than b return a value 
   else return b;// otherwise, return b 
  
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//I: number 
//O: boolean 
//C: recursion
//E: Conditional 
function isEven(n) { // function w/ number as param 
  if (n == 0) return true; // if value of n is loosely equal 0; rt true 
  else if (n == 1) return false; // loosely equal to 1 return false 
  else if (n < 0) return isEven(-n); // negative # return n -1  
  else return isEven(n - 2); // // return n -2 
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//I: String
//O: number
//C: N/A
//E: for loop


function countChars(string,ch) { // string and character as param 
let counted = 0; // valur 0 assigned to counted 
for (let i = 0; i < string.length; i++){ // for loop over string entirety 
  if(string[i] == ch) { // if value from string loosely equals character, add 1 to counter updating per increment 
    counted +=1;
  }
}
return counted;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//I: a string 
//O: # of string char "B"
//C: N/A
//E: N/A
function countBs(string) {
  return countChars(string, "B");
}


